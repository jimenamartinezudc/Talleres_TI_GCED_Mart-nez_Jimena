def compute_dct_matrix() -> npt.NDArray[np.float32]:
    u_matrix = np.zeros((8, 8)).astype(np.float32)
    colIncrement = 2
    u_matrix[0, :] = np.sqrt(2) / 4
    for rowIndex in range(1, 8):
        factor = rowIndex
        for colIndex in range(8):
            u_matrix[rowIndex, colIndex] = 0.5 * np.cos(factor * np.pi / 16)
            factor += colIncrement
        colIncrement += 2
    return u_matrix

def scale_quantization_matrix(quantization_matrix: npt.NDArray[np.float32], quality):
    if quality <0 or quality > 100:
        raise ValueError("Quality must be between 0 and 100")

    if quality <= 50:
        scale_factor = 50/quality
    else:
        scale_factor = 2 - quality/50

    return np.maximum(np.round(quantization_matrix*scale_factor), 1)

