def jpeg_coded_image_size(bit_str: list[list[str]], img_dimensions: tuple[int, int], channels=1) -> float:
    flat_list = [symbol for block in bit_str for symbol in block]
    num_bits = len("".join(flat_list))
    # bits required by the Huffman Tables
    # AC table has 160 codes and we need 2 bytes per code
    # Luminance and chrominance tables are different
    num_bits += 2 * 8 * 160 * (1 + (channels == 3))

    # DC table has 12 entries and we need also 2 bytes per entry
    # Luminance and chrominance tables are different
    num_bits += 2 * 8 * 12 * (1 + (channels == 3))

    # Quantification matrices have 64 elements and require 1 byte per entry
    num_bits += 8 * 64 * (1 + (channels == 3))

    # bits/pixel are obtained dividing by the total number of pixels of the
    # image.
    return num_bits / (img_dimensions[0] * img_dimensions[1])

    

def print_matrix(mat):
    for row in mat:
        formatted_row = "\t".join(f"{x:.2f}" if isinstance(x, np.float32) else str(x) for x in row)
        print(formatted_row)
