def huffman_decimal_to_binary(d: int) -> str:
    if d == 0:
        return "0"
    binary_d = bin(abs(d))[2:]
    if d > 0:
        return binary_d
    else:
        return "".join('1' if b == '0' else '0' for b in binary_d)

def huffman_binary_to_decimal(bin_str: str, is_zero=False) -> int:
    if is_zero:
        return 0

    if bin_str[0] == '1':
        return int(bin_str, 2)

    inverted_bits = "".join('1' if b == '0' else '0' for b in bin_str)
    return -int(inverted_bits, 2)

