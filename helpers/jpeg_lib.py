def load_image_RGB_and_YCrCb(image_name : str) -> Tuple[Image.Image, Image.Image]:
    # Cargar la imagen en formato RGB
    image_rgb : Image.Image = Image.open(image_name).convert('RGB')

    # Convertir de RGB a YCbCr
    image_ycbcr : Image.Image = image_rgb.convert('YCbCr')

    return image_rgb, image_ycbcr

